package Array2D;

import java.util.*;

public class Array2DD {
    public static void main(String [] args) {
        String[][] n = {{},{"1","2","3"},{"5"}};
        System.out.println("=========>> TOTAL ROW: " + n.length + " <<=========");
        System.out.println("Column number of first row: " + n[0].length);
        System.out.println("Column number of second row: " + n[1].length);
        System.out.println("Column number of third row: " + n[2].length);
    }
}
